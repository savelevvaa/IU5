// MathFuncsLib.cpp
#include "MathFuncsLib.h"
using namespace std;

namespace MathFuncs
{
    double MyMathFuncs::Add(double a, double b)
    {
        return a + b;
    }

    double MyMathFuncs::Subtract(double a, double b)
    {
        return a - b;
    }
	double MyMathFuncs::Multiply(double a, double b)
	{
		return a * b;
	}
	double MyMathFuncs::Devide(double a, double b)
	{
		return a / b;
	}

}